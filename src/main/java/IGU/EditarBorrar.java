    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if(tablaAutos.getRowCount() > 0){
            //valida que se haya seleccionado un registro
            if(tablaAutos.getSelectedRow() != -1){
                
                Long idAuto = Long.valueOf(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                
                FormEdicion pantalla = new FormEdicion(idAuto);
                pantalla.setVisible(true);
                pantalla.setLocationRelativeTo(null);
                
                this.dispose();
            }else{
                mostrarMensaje("No lo seleccionó un registro", "Error" ,"Error al Modificar");
            }
        }else{
            mostrarMensaje("Tabla Vacia", "Error", "Error al Modificar");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //verificar que la tabla no esté vacia
        if(tablaAutos.getRowCount() > 0){
            //valida que se haya seleccionado un registro
            if(tablaAutos.getSelectedRow() != -1){
                //obtener id
                Long idAuto = Long.valueOf(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                System.out.println("ID long" + idAuto);
                controladora.borrarAuto(idAuto);
                mostrarMensaje("Automovil Borrado Correctamente", "Info", "Borrado Exitoso!");
                cargarTabla(); 
            }else{
                mostrarMensaje("No lo seleccionó un registro", "Error" ,"Error al eliminar");
            }
        }else{
            mostrarMensaje("Tabla Vacia", "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    
    
