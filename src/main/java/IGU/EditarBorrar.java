    private void cargarTabla() {
        DefaultTableModel tabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
    
        //titulos a la columna
        String titulos[] = {"Id", "Modelo", "Marca", "Motor", "Color", "Patente", "Cant. Puertas"};
        tabla.setColumnIdentifiers(titulos);
        //traer los datos de la BBDD
        List<Automovil> listAutomovil = controladora.traerAutos();
        //setear la lista a la tabla
        if(listAutomovil != null){
            for(Automovil auto : listAutomovil){
                Object[] object = {
                    auto.getId(), 
                    auto.getModelo(),
                    auto.getMarca(),
                    auto.getMotor(),
                    auto.getColor(),
                    auto.getPatente(),
                    auto.getCantPuertas()
                };
                
                tabla.addRow(object);
                
            }
        }
        
        tablaAutos.setModel(tabla);
    }
